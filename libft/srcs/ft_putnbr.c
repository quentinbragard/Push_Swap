#include "libft.h"

void	ft_putnbr(int n)
{
	if ((n >= 0) && (n <= 9))
		ft_putchar(48 + n);
	if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putnbr(214748364);
		ft_putnbr(8);
	}
	if ((n < 0) && (n != -2147483648))
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
}
