#include "libft.h"

void	ft_strstrdel(char ***tab)
{
	int i;

	i = 0;
	if (!(*tab))
		return ;
	while ((*tab)[i])
		ft_strdel(&(*tab)[i++]);
	free(*tab);
	*tab = NULL;
}
